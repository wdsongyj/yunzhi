        /// <summary>
        /// 构造分页语句
        /// </summary>
        /// <param name="pageSize"></param>
        /// <param name="pageIndex"></param>
        /// <param name="whereClause"></param>
        /// <param name="orderByClause"></param>
        /// <param name="allColumns"></param>
        /// <param name="tableName"></param>
        /// <param name="tableAlias"></param>
        /// <returns></returns>
        public string BuildPageSql(int pageSize, int pageIndex, string whereClause, string orderByClause, string allColumns, string tableName, string tableAlias)
        {
            if (tableAlias == null) tableAlias = "";
            if (whereClause == null)
            {
                whereClause = "";
            }
            else if (whereClause != "")
            {
                whereClause = " where " + whereClause;
            }
            int endNumber = pageSize * (pageIndex + 1);
            int startNumber = pageIndex * pageSize + 1;
            string sql = "SELECT " + allColumns +
                " FROM (SELECT TOP " + endNumber + " *,Row_number() over (order by " + orderByClause +
                ") as rownumber FROM " + tableName + " " + tableAlias + whereClause +
                " ORDER BY " + orderByClause +
                ") PageTable where rownumber <= " + endNumber + " and rownumber >= " + startNumber;
            return sql;
        }

